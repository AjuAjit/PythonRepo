def print_diff_number_formats(deci, octa, hexa):
    print("Decimal number = {}".format(deci))
    print("Octal number = {}".format(octa))
    print("Hexadecimal number = {}".format(hexa))
    

# three types of number formats are printed
deci = input("Enter the decimal number: ")
octa = input("Enter the octal number: ")
hexa = input("Enter the hexadecimal number: ")
print_diff_number_formats(deci, octa, hexa)



